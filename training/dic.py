
"""SIMPLE DICTIONARY OPERATIONS"""

"""Initial dictionary"""

dic={"name" : "alec", "letter" : "apple"}
print(dic)

"""Call the parameter from dictionary"""
result = dic["name"]
print(result)

"""Delete parameter from the dictionary"""
dic.pop("name")
print(dic)

""" Add parameter to the dictionary"""
dic["name"] ="John"
print(dic)

""" Change parameter in the dictionary"""
dic["name"] = "Alex"
print(dic)




